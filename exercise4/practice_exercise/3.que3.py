# 3. Find the first and last position of 'l' in 'Hello World'.

a="HeLLo World"
x=a.find('L')
y=a.rfind('L')
print("from left: {0} from right: {1}".format(x,y))
print("from left: {0} from right: {1}".format(x,y))
print("from right: {1} from left: {0}".format(x,y))

print(f"from left {x} from right {y}")
